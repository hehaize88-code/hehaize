for (const [locale, generated] of Object.entries(generatedTranslations)) {
  translations[locale] = { ...generated, ...translations[locale] };
}

for (const [locale, additions] of Object.entries(supplementalTranslations)) {
  Object.assign(translations[locale], additions);
}

export function getLanguage(code) {
  return languages.find((language) => language.code === code) || languages[0];
}

export function translateExact(value, locale) {
  if (!value || locale === "en") return value;
  return translations[locale]?.[value] || value;
}

const reverseTranslations = Object.values(translations).reduce((reverse, dictionary) => {
  for (const [english, translated] of Object.entries(dictionary)) reverse.set(translated, english);
  return reverse;
}, new Map());

export function getEnglishSource(value) {
  return reverseTranslations.get(value) || value;
}
